package TeachingMaterials

fun main() {
    // Loops in Kotlin

    // For Loop
    for (i in 1..5) {
        println("For Loop: $i")
    }

    // While Loop
    var j = 1
    while (j <= 5) {
        println("While Loop: $j")
        j++
    }

    // Do-While Loop
    var k = 1
    do {
        println("Do-While Loop: $k")
        k++
    } while (k <= 5)
}
